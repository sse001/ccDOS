# Basic calculator for adding whole numbers
class basicCalculator:
	def basicNumberInput():
		imint = int(input("Insert Number: "))
		return imint

	def basicAddition(n1, n2):
		num3 = n1 + n2
		return num3

	def basicSubtraction(n1, n2):
		num3 = n1 - n2
		return num3

	def basicMultiplication(n1, n2):
		num3 = n1 * n2
		return num3

	def basicDivision(n1, n2):
		num3 = n1 / n2
		return num3
